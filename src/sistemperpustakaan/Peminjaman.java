    private void nomoran() {
        //throw new UnsupportedOperationException("Not yet implemented");
        String vtfidang = tfidang.getText(); 
        String sql = "select right (No_Pinjam,2) as no from peminjaman where No_Pinjam like '"+tfidang.getText()+"%' order by No_Pinjam asc";
        
        try {
            Statement pst = conec.prepareStatement(sql);
            Statement stat = conec.createStatement();
            ResultSet rs = pst.executeQuery(sql);
            if (rs.first()==false)
            {
                tfpinjam.setText(vtfidang + "01");
                cc=1;
            }
            else {
                rs.last();
                int no = rs.getInt(1)+1;
                String kbinasi = String.valueOf(no);
                int pjg_kbinasi = kbinasi.length();
                String jebot = "";
                for(int i=0; i<2-pjg_kbinasi;i++){
                   // kbinasi = "0"+kbinasi;
                    jebot = jebot+"0";
                }
                tfpinjam.setText(vtfidang+jebot+kbinasi);
                cc = Integer.parseInt(kbinasi);
            }
            rs.close();
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan fatal2 :"+e);
            
        }
    }
    
