    private void tblsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tblsaveActionPerformed
        // TODO add your handling code here:
        //int cc = Integer.valueOf(tfpinjam.getText());
        String vnop  =tfpinjam.getText();
        String vcopy =tfpinjam.getText();
        String vtglp =tftgl.getText();
        String vtglk =tftglbalik.getText();
        String vkda  =tfidang.getText();
        String vidcopy =tfidcopy.getText();
        String hhh = a.getText();
               
        if (tfpinjam.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"Data Masih Kosong!","PERHATIAN",JOptionPane.WARNING_MESSAGE);
            tfpinjam.requestFocus();
        }else if (tfidcopy.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"Data Masih Kosong!","PERHATIAN",JOptionPane.WARNING_MESSAGE);
            tfidcopy.requestFocus();
        }
        
        if (a.getText().equals("Terpinjam")){
            JOptionPane.showMessageDialog(null,"Buku sudah terpinjam, silahkan cari buku lain","PERHATIAN",JOptionPane.WARNING_MESSAGE);
            tfidcopy.requestFocus();
        }else{
            try {
                java.sql.Statement stat = conec.createStatement();
                
                ResultSet data=stat.executeQuery("SELECT * FROM peminjaman WHERE No_Pinjam='"+tfpinjam.getText()+"'");
                if (data.next()){
                    JOptionPane.showMessageDialog(null,"No Pinjam sudah dimasukan","PERHATIAN",JOptionPane.WARNING_MESSAGE);
                    tfpinjam.requestFocus();
                    
                
                }
                else {
                    String squ = "Update copybuku set Statusbuku='Terpinjam' "
                            + "where ID_Copybuku='"+tfidcopy.getText()+"'";
                    stat.executeUpdate(squ);
                    try {
                        //Statement stat=conec.createStatement();
                        String sql2 = "SELECT * FROM copybuku where ID_Copybuku='"+tfidcopy.getText()+"'" ;
                        ResultSet rp=stat.executeQuery(sql2);

                        if(rp.next()){
                            
                            String sql ="INSERT INTO peminjaman VALUE('"+vnop+"','"+vtglp+"','"+vtglk+"','"+vkda+"','"+vidcopy+"','"+rp.getString("Statusbuku")+"','"+cc+"')";
                            stat.executeUpdate(sql);

                        }else{
                            System.out.println("gagal");
                        }
                        }

                    catch(Exception exc){
                        System.err.println("Terjadi Kesalahan : "+exc);
                    }
                    
                    
                    
                    tfpinjam.setText("");
                    tfidang.setText("");
                    tfnip.setText("");
                    tfnama.setText("");
                    tfjenis.setText("");
                    tfidcopy.setText("");
                    tfjudul.setText("");
                    a.setText(""); 
                    //cc=0;
                    //tampilData();
                    time();
                    tabs();
                    {
                            
                	JOptionPane.showMessageDialog(null," Berhasil Di Simpan");
                        
		}
		    tfidang.requestFocus();
                }
                        
                stat.close();
            }
            catch(Exception exc){
                System.err.println("Terjadi kesalahan fatal :"+exc);
            }
            nomoran();
        }
    }//GEN-LAST:event_tblsaveActionPerformed

